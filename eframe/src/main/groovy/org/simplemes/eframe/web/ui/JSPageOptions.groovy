/**
 * Defines the page options for javascript client pages.  These are mainly the constants used in the code to get/set
 * page options such as the label width.  This includes default values for the constants.
 */
class JSPageOptions {

  /**
   * The name of the page option that controls the width of the label column in a form (<b>Value:</b> 'labelWidth').
   */
  static final String LABEL_WIDTH_NAME = 'labelWidth'

  /**
   * The default value for the page option that controls the width of the label column in a form (<b>Value:</b> '20%').
   */
  static final String LABEL_WIDTH_DEFAULT = '20%'


}
